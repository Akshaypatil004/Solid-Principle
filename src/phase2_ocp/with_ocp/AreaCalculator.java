package phase2_ocp.with_ocp;

public class AreaCalculator {
	
	public double calculateArea(Shape shape) {
		return shape.area();
		// That's it. One line. Done.
	    // No if-else. No instanceof. No switch.
	    // It doesn't CARE which shape it is.
	    // It just asks: "whatever you are, tell me your area."
	}

}
